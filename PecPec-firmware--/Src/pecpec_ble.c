/** \brief Send string over BLE UART
 *
 * \param p_string uint8_t* - string to send
 * \param length uint16_t - size of string
 * \return uint32_t - return code
 *
 */
ret_code_t ble_send(uint8_t * p_string, uint16_t length){
    uint32_t err_code = NRF_SUCCESS;
    do
    {
        err_code = ble_nus_string_send(&m_nus, p_string, length);

        if ( (err_code != NRF_ERROR_INVALID_STATE) && (err_code != NRF_ERROR_BUSY) )
        {
            APP_ERROR_CHECK(err_code);
        }
    }
    while (err_code == NRF_ERROR_BUSY);
    return err_code;
}


/**@brief Function for simulating and sending one Temperature Measurement.
 */
void temperature_measurement_send(void)
{
    ble_hts_meas_t simulated_meas;
    ret_code_t     err_code;

    printf("TEMP_SEND\r\n");
    if (!m_hts_meas_ind_conf_pending)
    {

        hts_sim_measurement(&simulated_meas);
        //simulated_meas.temp_in_celcius.exponent = 1;
        //simulated_meas.temp_in_celcius.mantissa = 33;
        err_code = ble_hts_measurement_send(&m_hts, &simulated_meas);

        switch (err_code)
        {
            case NRF_SUCCESS:
                // Measurement was successfully sent, wait for confirmation.
                m_hts_meas_ind_conf_pending = true;
                break;

            case NRF_ERROR_INVALID_STATE:
                // Ignore error.
                break;

            default:
                APP_ERROR_HANDLER(err_code);
                break;
        }
    }
}


